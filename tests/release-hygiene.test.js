test("generated root page is self-verifying and has no wall-clock build noise", () => {
  const indexSource = read("index.html");
  const marker = indexSource.match(
    /^<!doctype html>\n<!-- @generated by scripts\/build-index\.mjs; artifact-sha256=([a-f0-9]{64}); do not edit -->\n/
  );
  assert.ok(marker, "generated artifact marker is required");
  const normalizedBody = indexSource.replace(marker[0], "<!doctype html>\n");
  const artifactHash = crypto.createHash("sha256").update(normalizedBody).digest("hex");
  assert.equal(marker[1], artifactHash);
  assert.match(indexSource, /const EXPORTED_STATE_BLUEPRINT_SAVED_AT = "1970-01-01T00:00:00\.000Z";/);
  assert.equal((indexSource.match(/@generated by scripts\/build-index\.mjs/g) || []).length, 1);
});

test("public demo exposes only the qualified Managed Pilot offer", () => {
  const indexSource = read("index.html");
  const blueprintSource = indexSource.match(/const EXPORTED_STATE_BLUEPRINT = (\{.*\});/)?.[1];
  assert.ok(blueprintSource, "exported public blueprint is required");
  const model = JSON.parse(blueprintSource);
  const pricing = model.states.find(state => state.id === "site_pricing");
  const qualification = model.states.find(state => state.id === "site_checkout");
  const approach = model.states.find(state => state.id === "site_login");
  const acceptance = model.states.find(state => state.id === "site_profile");
  const home = model.states.find(state => state.id === "site_home");
  const publicModel = JSON.stringify(model);

  assert.deepEqual(pricing.data.plans.plans.map(plan => plan.title), ["Managed Pilot"]);
  assert.equal(pricing.data.plans.plans[0].price, "2.500–7.500 €");
  assert.equal(pricing.data.plans.plans[0].duration, "6–12 Wochen");
  assert.equal(qualification.data.qualification.prepared, false);
  assert.equal(approach.title, "Vorgehen");
  assert.equal(acceptance.title, "Abnahme");
  assert.equal(home.data.hero.actionLabel, "Editor öffnen");
  assert.equal(home.data.hero.url, "/state.html");
  assert.equal(home.data.hero.secondaryActionLabel, "Pilot ansehen");
  assert.equal(home.data.hero.secondaryTransitionId, "site_home_nav_pricing");
  assert.ok(!("order" in qualification.data));
  assert.ok(!("email" in approach.data.hero));
  assert.ok(!("password" in approach.data.hero));
  assert.ok(!("account" in acceptance.data));
  assert.doesNotMatch(publicModel, /(?:249|749|1\.990) EUR|\/Monat|Starter anfragen|Business anfragen|Scale anfragen|BI zubuchen/);
});

