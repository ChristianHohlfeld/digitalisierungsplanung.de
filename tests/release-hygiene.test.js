test("local Markdown links in product and operating documents resolve", () => {
  const documents = [
    "README.md",
    "SECURITY.md",
    "server/README.md",
    ...fs.readdirSync(path.join(ROOT, "docs"))
      .filter(name => name.endsWith(".md"))
      .map(name => `docs/${name}`),
    ...fs.readdirSync(path.join(ROOT, "docs/product")).map(name => `docs/product/${name}`),
    ...fs.readdirSync(path.join(ROOT, "docs/operations")).map(name => `docs/operations/${name}`)
  ];
  for (const relativePath of documents) {
    const directory = path.dirname(path.join(ROOT, relativePath));
    for (const match of read(relativePath).matchAll(/\[[^\]]*\]\(([^)]+)\)/g)) {
      const target = match[1].split("#", 1)[0].trim();
      if (!target || /^(?:https?:|mailto:)/i.test(target)) continue;
      const resolved = path.resolve(directory, decodeURIComponent(target));
      assert.ok(resolved.startsWith(ROOT), `${relativePath} link escapes repository: ${target}`);
      assert.ok(fs.existsSync(resolved), `${relativePath} has a broken link: ${target}`);
    }
  }
});

function git(cwd, args) {
  const result = spawnSync("git", args, { cwd, encoding: "utf8" });
  assert.equal(result.status, 0, result.stderr || result.stdout);
  return result.stdout.trim();
}

function releaseStamp(sequence, sourceCommit) {
  return `globalThis.ZUSTAND_RELEASE_SEQUENCE = ${sequence};\n` +
    `globalThis.ZUSTAND_RELEASE_ID = "release-${sequence}";\n` +
    `globalThis.ZUSTAND_RELEASE_BUILT_AT = "2026-07-15T10:00:00Z";\n` +
    `globalThis.ZUSTAND_RELEASE_SOURCE = "${sourceCommit}";\n`;
}

function runReleasePlanner(repo, sequence, verifiedCommit) {
  const envFile = path.join(repo, "github-env.txt");
  const result = spawnSync(process.execPath, [path.join(ROOT, "scripts/plan-managed-release.mjs")], {
    cwd: repo,
    encoding: "utf8",
    env: {
      ...process.env,
      RELEASE_REPO: repo,
      REQUESTED_SEQUENCE: String(sequence),
      VERIFIED_SOURCE_COMMIT: verifiedCommit,
      GITHUB_ENV: envFile
    }
  });
  return {
    ...result,
    plan: result.status === 0 ? JSON.parse(result.stdout) : null,
    environment: fs.existsSync(envFile) ? fs.readFileSync(envFile, "utf8") : ""
  };
}

test("managed release planning resumes tag-only and main-only partial publication safely", () => {
  const root = fs.mkdtempSync(path.join(os.tmpdir(), "digitalisierungsplanung-release-plan-"));
  const remote = path.join(root, "origin.git");
  const repo = path.join(root, "repo");
  try {
    fs.mkdirSync(repo);
    git(root, ["init", "--bare", remote]);
    git(repo, ["init", "--initial-branch=main"]);
    git(repo, ["config", "user.name", "Release Test"]);
    git(repo, ["config", "user.email", "release@example.test"]);
    fs.writeFileSync(path.join(repo, "release-version.js"), releaseStamp(40, "0".repeat(40)));
    fs.writeFileSync(path.join(repo, "app.txt"), "verified source\n");
    git(repo, ["add", "."]);
    git(repo, ["commit", "-m", "verified source"]);
    git(repo, ["remote", "add", "origin", remote]);
    git(repo, ["push", "-u", "origin", "main"]);
    git(repo, ["fetch", "--tags", "origin", "main"]);
    const sourceCommit = git(repo, ["rev-parse", "HEAD"]);

    const fresh = runReleasePlanner(repo, 41, sourceCommit);
    assert.equal(fresh.status, 0, fresh.stderr);
    assert.equal(fresh.plan.mode, "create");
    assert.equal(fresh.plan.releaseSourceCommit, sourceCommit);
    assert.match(fresh.environment, /^RELEASE_MODE=create$/m);

    fs.writeFileSync(path.join(repo, "release-version.js"), releaseStamp(41, sourceCommit));
    git(repo, ["add", "release-version.js"]);
    git(repo, ["commit", "-m", "Release release-41"]);
    const releaseCommit = git(repo, ["rev-parse", "HEAD"]);
    git(repo, ["tag", "-a", "release-41", "-m", "release 41"]);

    // Simulate a non-atomic legacy push where only the tag reached the remote.
    git(repo, ["push", "origin", "refs/tags/release-41"]);
    git(repo, ["reset", "--hard", sourceCommit]);
    git(repo, ["fetch", "--tags", "--prune", "origin", "main"]);
    fs.rmSync(path.join(repo, "github-env.txt"), { force: true });
    const tagOnly = runReleasePlanner(repo, 41, sourceCommit);
    assert.equal(tagOnly.status, 0, tagOnly.stderr);
    assert.equal(tagOnly.plan.mode, "resume-tagged");
    assert.equal(tagOnly.plan.releaseCommit, releaseCommit);

    // Complete main, then remove the tag to model the inverse partial failure.
    git(repo, ["push", "origin", `${releaseCommit}:refs/heads/main`]);
    git(repo, ["reset", "--hard", releaseCommit]);
    git(repo, ["fetch", "origin", "main"]);
    fs.rmSync(path.join(repo, "github-env.txt"), { force: true });
    const complete = runReleasePlanner(repo, 41, releaseCommit);
    assert.equal(complete.status, 0, complete.stderr);
    assert.equal(complete.plan.mode, "resume-complete");

    git(remote, ["update-ref", "-d", "refs/tags/release-41"]);
    git(repo, ["tag", "-d", "release-41"]);
    git(repo, ["fetch", "--prune", "origin", "main"]);
    fs.rmSync(path.join(repo, "github-env.txt"), { force: true });
    const mainOnly = runReleasePlanner(repo, 41, releaseCommit);
    assert.equal(mainOnly.status, 0, mainOnly.stderr);
    assert.equal(mainOnly.plan.mode, "resume-main");
    assert.equal(mainOnly.plan.releaseCommit, releaseCommit);

    // A conflicting tag is never overwritten or silently treated as resumable.
    git(repo, ["tag", "-a", "release-41", sourceCommit, "-m", "conflict"]);
    fs.rmSync(path.join(repo, "github-env.txt"), { force: true });
    const conflict = runReleasePlanner(repo, 41, releaseCommit);
    assert.notEqual(conflict.status, 0);
    assert.match(conflict.stderr, /different commit|single stamp-only child|incompatible release stamp/);
  } finally {
    fs.rmSync(root, { recursive: true, force: true });
  }
});
